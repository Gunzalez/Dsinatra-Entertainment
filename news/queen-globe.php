                </div>
                <div class="copy">

                    <p>All of Asaba was set agog when 23 year old UNIBEN Undergraduate <strong>Olumilua Yemi</strong> from Ekiti State won the most coveted crown of Queen Globe Beauty Pageant 2013.</p>
                    <p>She beat 25 other contestants to win the 2013 Queen Globe Beauty pageant which held at the Grand Hotel in Asaba Delta state on Saturday August 17th. Yemi will now represent Nigeria at the Miss Globe International pageant.</p>
                    <p>The event was held at Grand Hotel August 17th 2013, Asaba and according to Hon Odi Odigie, CEO of D.Sinatra Ent promised an <a href="../gallery3.php">electrifying show</a>!</p>
                    <p>Queen Globe is committed to empowering young beauties, we are not selling a dream, we are building a future for the young ladies who are contesting in our pageant. We are confident that the new Queen Globe Beauty Pageant which produces avenue for the country to have its representative in the following international competition which includes Miss Globe International, Miss West Africa International, World Super Model and Teen Super Model will benefit the country and move us all a step closer toward building a greater Nigeria.</p>
                    <p><strong>Winners -</strong></p>
                    <ul>
                        <li><strong>Miss Olumilua Yemi</strong> - Queen Globe, to represent at Miss Globe International in Cyprus</li>
                        <li><strong>Miss Emmanuel Adekemi</strong> - Miss West Africa Nigeria, to represent Miss West Africa International in Cape Verde</li>
                        <li><strong>Miss Osemwegie Beauty</strong> - World Super Model Nigeria, to represent at World Super Model in Fiji Island</li>
                        <li><strong>Miss Okeke Queen</strong> - Teen Super Model Nigeria, to represent at Teen Super Model in Fiji Island</li>
                        <li><strong>Patricia Peters Seigha</strong> - Miss Amity Queen Globe</li>
                        <li><strong>Akpofure Onajite</strong> - Miss Photogenic Queen Globe</li>
