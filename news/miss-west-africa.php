	                <h2 class="clearfix"><span>Sophie-Ann Diete-Spiff - Miss West Africa Nigeria</span></h2>
                </div>
                <div class="copy">
                    <p>On the 23rd of Feb 2013, 26 contestants gathered at Grand Hotel, Asaba, Delta State for the Miss West Africa Nigeria beauty pageant.</p>
                    <p>The event was put together by D.Sinatra ENT in collaboration with Miss West Africa international and saw Uti Nwachukwu and Ejiro of Kiss FM Abuja as host, fast rising comedian Frank D Don was present in the house, holding the crowd to great jokes.</p>
                    <p>Various performance from Slim Joe, Sele Bobo and Meshack thrilled the audience. The <a href="../gallery2.php">show</a> hit a climax with a cameo appearance of Lynxxx (Mr Utunu) performing songs like "change your parade" and "fine, fine lady", Lynxxx also used the opportunity to let the crowd know he is an Asaba indigene.</p>
                    <p><strong>Sophie-Ann Diete-Spiff</strong> who hails from Baylesa State, emerged the winner and was crowned Miss West Africa Nigeria while Ikekhuame Rachael and Isiofia Chioma Stephanie ended as 1st and 2nd runner up respectively. Omone Otoide won Miss Photogenic not forgetting Queen Celestine won Miss Amity.</p>
                    <p>The Miss West African international Queen, Vanny Reis was present to crown the newly emerged Queen-Sophie-Ann Diete-Spiff, who went home with a brand new Kia Rio 2012 model, undisclosed cash prize and lots more from various partners of the event.</p>
                    <p>Queen Sophie-Ann Diete-Spiff will be representing Nigeria at the Miss West Africa International event which comes up on the 23rd of March 2013 at Cape Verde.</p>
                    <p>Read her <a href="http://www.dsinatraent.com/blog/my-miss-west-african-nigeria-diary-sophie-ann-diete-spiff/">blog entry</a>.</p>
